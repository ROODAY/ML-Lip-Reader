public class FrameObject {

    private String frameData;

    public FrameObject() {
    }

    public FrameObject(String frameData) {
        super();
        this.frameData = frameData;
    }

    public String getFrameData() {
        return frameData;
    }
    public void setFrameData(String frameData) {
        this.frameData = frameData;
    }
}